#include "push_swap.h"

char	*ft_strdup(const char *s)
{
	char	*p;
	size_t	i;
	size_t	l;

	l = ft_strlen(s);
	p = (char *)malloc(sizeof(char) *(l + 1));
	if (!p)
		return (NULL);
	i = 0;
	while (s[i])
	{
		p[i] = s[i];
		i++;
	}
	p[i] = '\0';
	return (p);
}
